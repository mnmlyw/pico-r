// PICO-8 built-in 4x6 font for P8SCII 0-255.
// Each glyph: 3 bytes encoding 6 rows of 4 pixels (high nibble = even row).

pub const FONT_DATA: [[u8; 3]; 256] = build_font();

const fn build_font() -> [[u8; 3]; 256] {
    let mut data = [[0u8; 3]; 256];

    data[32] = [0x00, 0x00, 0x00];
    data[33] = [0x44, 0x40, 0x40];
    data[34] = [0xAA, 0x00, 0x00];
    data[35] = [0xAF, 0xAF, 0xA0];
    data[36] = [0x4E, 0xC2, 0xE4];
    data[37] = [0xA2, 0x44, 0x8A];
    data[38] = [0x4A, 0x4A, 0x50];
    data[39] = [0x44, 0x00, 0x00];
    data[40] = [0x24, 0x44, 0x20];
    data[41] = [0x84, 0x44, 0x80];
    data[42] = [0xA4, 0xEA, 0x40];
    data[43] = [0x04, 0xE4, 0x00];
    data[44] = [0x00, 0x00, 0x48];
    data[45] = [0x00, 0xE0, 0x00];
    data[46] = [0x00, 0x00, 0x40];
    data[47] = [0x22, 0x44, 0x80];

    data[b'0' as usize] = [0xEA, 0xAA, 0xE0];
    data[b'1' as usize] = [0x4C, 0x44, 0xE0];
    data[b'2' as usize] = [0xE2, 0xE8, 0xE0];
    data[b'3' as usize] = [0xE2, 0xE2, 0xE0];
    data[b'4' as usize] = [0xAA, 0xE2, 0x20];
    data[b'5' as usize] = [0xE8, 0xE2, 0xE0];
    data[b'6' as usize] = [0xE8, 0xEA, 0xE0];
    data[b'7' as usize] = [0xE2, 0x24, 0x40];
    data[b'8' as usize] = [0xEA, 0xEA, 0xE0];
    data[b'9' as usize] = [0xEA, 0xE2, 0xE0];

    data[b':' as usize] = [0x04, 0x04, 0x00];
    data[b';' as usize] = [0x04, 0x04, 0x80];
    data[b'<' as usize] = [0x24, 0x84, 0x20];
    data[b'=' as usize] = [0x0E, 0x0E, 0x00];
    data[b'>' as usize] = [0x84, 0x24, 0x80];
    data[b'?' as usize] = [0xE2, 0x40, 0x40];
    data[b'@' as usize] = [0x4A, 0xEE, 0x80];

    let alpha: [(u8, [u8; 3]); 26] = [
        (b'A', [0x4A, 0xEA, 0xA0]),
        (b'B', [0xCA, 0xCA, 0xC0]),
        (b'C', [0x68, 0x88, 0x60]),
        (b'D', [0xCA, 0xAA, 0xC0]),
        (b'E', [0xE8, 0xE8, 0xE0]),
        (b'F', [0xE8, 0xE8, 0x80]),
        (b'G', [0xE8, 0xAA, 0xE0]),
        (b'H', [0xAA, 0xEA, 0xA0]),
        (b'I', [0xE4, 0x44, 0xE0]),
        (b'J', [0x22, 0x2A, 0x40]),
        (b'K', [0xAA, 0xCA, 0xA0]),
        (b'L', [0x88, 0x88, 0xE0]),
        (b'M', [0xAE, 0xEA, 0xA0]),
        (b'N', [0xAE, 0xEE, 0xA0]),
        (b'O', [0x4A, 0xAA, 0x40]),
        (b'P', [0xEA, 0xE8, 0x80]),
        (b'Q', [0x4A, 0xAE, 0x60]),
        (b'R', [0xEA, 0xEC, 0xA0]),
        (b'S', [0x68, 0x42, 0xC0]),
        (b'T', [0xE4, 0x44, 0x40]),
        (b'U', [0xAA, 0xAA, 0xE0]),
        (b'V', [0xAA, 0xAA, 0x40]),
        (b'W', [0xAA, 0xEE, 0xA0]),
        (b'X', [0xAA, 0x4A, 0xA0]),
        (b'Y', [0xAA, 0x44, 0x40]),
        (b'Z', [0xE2, 0x48, 0xE0]),
    ];
    let mut i = 0;
    while i < alpha.len() {
        data[alpha[i].0 as usize] = alpha[i].1;
        // Lowercase same as uppercase
        data[(alpha[i].0 + 32) as usize] = alpha[i].1;
        i += 1;
    }

    data[b'[' as usize] = [0x64, 0x44, 0x60];
    data[b'\\' as usize] = [0x88, 0x44, 0x20];
    data[b']' as usize] = [0xC4, 0x44, 0xC0];
    data[b'^' as usize] = [0x4A, 0x00, 0x00];
    data[b'_' as usize] = [0x00, 0x00, 0xE0];
    data[b'`' as usize] = [0x84, 0x00, 0x00];

    data[b'{' as usize] = [0x24, 0x84, 0x20];
    data[b'|' as usize] = [0x44, 0x44, 0x40];
    data[b'}' as usize] = [0x84, 0x24, 0x80];
    data[b'~' as usize] = [0x5A, 0x00, 0x00];
    data[127] = [0xAE, 0xEE, 0xA0];

    data[128] = [0x69, 0xF9, 0x60];
    data[129] = [0x46, 0xF6, 0x40];
    data[130] = [0x0E, 0xE0, 0x00];
    data[131] = [0x44, 0x44, 0x40];
    data[132] = [0x4E, 0xAA, 0xA0];
    data[133] = [0x04, 0xE4, 0x00];
    data[134] = [0x4E, 0x40, 0x00];
    data[135] = [0x46, 0xE4, 0x00];
    data[136] = [0x06, 0xE6, 0x00];
    data[137] = [0x06, 0xE4, 0x00];
    data[138] = [0x04, 0xE4, 0x00];
    data[139] = [0x04, 0xE4, 0x00];
    data[140] = [0x0C, 0xEC, 0x00];
    data[141] = [0x0C, 0xE4, 0x00];
    data[142] = [0x6F, 0xF6, 0x00];
    data[143] = [0xA4, 0xAA, 0x40];
    data[144] = [0x46, 0xE6, 0x40];
    data[145] = [0x0A, 0xE0, 0x00];
    data[146] = [0x4A, 0x40, 0x00];
    data[147] = [0x4E, 0xEA, 0x00];
    data[148] = [0xE8, 0xE2, 0xE0];
    data[149] = [0x6A, 0xAA, 0x60];
    data[150] = [0xAA, 0xAE, 0x40];
    data[151] = [0x04, 0xA4, 0x00];
    data[152] = [0x4E, 0xA4, 0x00];
    data[153] = [0x04, 0xE4, 0x00];
    data[154] = [0x69, 0x96, 0x00];
    data[155] = [0x6A, 0xA6, 0x00];
    data[156] = [0xAE, 0xEA, 0x00];
    data[157] = [0xE4, 0x4E, 0x00];
    data[158] = [0x4A, 0xE4, 0x00];
    data[159] = [0xE8, 0xCA, 0xE0];

    data
}

pub fn get_pixel(code: u8, x: u8, y: u8) -> bool {
    if x >= 4 || y >= 6 {
        return false;
    }
    let glyph = FONT_DATA[code as usize];
    let byte_idx = (y / 2) as usize;
    let nibble = if y.is_multiple_of(2) {
        glyph[byte_idx] >> 4
    } else {
        glyph[byte_idx] & 0x0F
    };
    nibble & (1 << (3 - x)) != 0
}
